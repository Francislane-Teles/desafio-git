#include <iostream> // Inclui uma biblioteca que permite mostrar textos no terminal

int main() {
    // A função principal do programa: é onde tudo começa
    std::cout << "Hello, World!" << std::endl; 
    // std::cout envia o texto para a tela (console)
    // "std" é o nome do "pacote" (namespace) padrão da linguagem C++
    // "endl" pula para a próxima linha (como o Enter)

    std::cout << "Você sabe usar o GitHub?" << std::endl; 
    // Mostra uma pergunta no terminal

    std::cout << "Você já criou um pull request hoje?" << std::endl;

    return 0; // Finaliza o programa
}
